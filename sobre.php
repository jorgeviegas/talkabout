		<h1><a id="p1">O que é o Talk About</a></h1>    
          <p> O Talk About é um sistema desenvolvido para que a população tenha um lugar para reivindicar os seus direitos. Ele oferece ao usuário a possibilidade de reclamar ou elogiar alguma instituição ou lugar. Essas reclamações são realizadas através de publicações, elas vão para uma timeline pública ou de algum grupo que o usuário pertence. Assim, outros usuários são capazes de visualizar sua publicação. </p>
    
        <h1> <a id="p2">Por que foi desenvolvido?</a></h1>
          <p> Com a grande dificuldade que a população encontra para conseguir usufruir de serviços/produtos de qualidade prestado pelas instituições, acreditamos que seja necessária a criação de um sistema que auxilie e dê voz para as pessoas reivindicarem seus direitos. Pesquisando sites (Ex: Reclame Aqui e Reclamão) que tratam do mesmo assunto, vimos que neles, não há muita interação entre usuários. 
		  <p> Com essa informação, vimos que é essencial que o sistema tenha funcionalidades para fazer a interação entre usuários, e que isso será o grande diferencial do Talk About. 
          Acreditamos que as pessoas, usando o sistema, terão mais liberdade e voz ativa para reclamar/elogiar uma instituição e também terão a chance de compartilhar suas ideias com pessoas que reivindicam uma mesma causa.  A criação de grupos de discussões e possibilidade de amizades serão motivos a mais para as pessoas usarem o sistema e lutar contra o abuso, monopólio e precariedade dos produtos e serviços prestados pelas empresas.</p>
          O sistema será de fácil uso para seus usuários, eles não encontrarão problemas para opinarem sobre algo e, com isso, poderá abranger todos os tipos de pessoas. O sistema visa contribuir para a sociedade, tentando mudar a realidade de hoje, onde as empresas tem um domínio sobre seus consumidores. Essa mudança será muito importante porque, muitas vezes, os consumidores perdem dinheiro, tempo e outras coisas importantes por causa das instituições.</p>
    
        <h1><a id="p3">Objetivo</a></h1>
          <p>- fazer com que as pessoas se comuniquem e reivindiquem seus direitos através de publicações e grupos, tanto reclamando quanto elogiando um local ou instituição que frequentam; </p>
		  <p>- mostrar para a administração desse local onde existem erros a serem corrigidos através do relato dos usuários; </p>
          <p>- divulgar críticas ou elogios de pessoas para um determinado serviço ou local; </p>
          <p>- proporcionar à administração das instituições uma fiscalização melhor em relação aos aspectos a melhorar e, também, conseguirá fazer uma aproximação entre usuários x administração.</p>
			
